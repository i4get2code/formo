# disable screen saver blanking
# xset s off

# change blank time to 1 hour
# xset s 3600 3600 	

# turn off dpms
# xset -dpms

# disable dpms and prevent screen from blanking
xset s off -dpms

# turn off screen immediately
# xset dpms force off

# standby screen
# xset dpms force standby

# suspend screen 
# xset dpms force suspend
